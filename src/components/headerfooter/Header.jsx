import { useState, useEffect, useRef } from "react";
import { Link, useNavigate } from "react-router-dom";
import logo from "../../assets/image/logo-english.jpg";
import "../headerfooter/Header.css";
import { login, register } from "../../api/api.js";
import { toast, ToastContainer, Slide } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import { jwtDecode } from "jwt-decode";

function Header() {
  const [showLogin, setShowLogin] = useState(false);
  const [mode, setMode] = useState("login");
  const [user, setUser] = useState(null);
  const [isDropdownOpen, setDropdownOpen] = useState(false);
  const [form, setForm] = useState({
    name: "",
    email: "",
    password: "",
    phone: "",
    address: "",
  });
  const modalRef = useRef();
  const dropdownRef = useRef();
  const navigate = useNavigate();

  useEffect(() => {
    const storedUser = localStorage.getItem("user");
    if (storedUser && storedUser !== "undefined") {
      try {
        setUser(JSON.parse(storedUser));
      } catch {
        localStorage.removeItem("user");
      }
    }
  }, []);

  const toggleLogin = () => setShowLogin((prev) => !prev);

  const handleClickOutside = (event) => {
    if (modalRef.current && !modalRef.current.contains(event.target)) {
      setShowLogin(false);
    }
  };

  const handleLogout = () => {
    localStorage.removeItem("token");
    localStorage.removeItem("user");
    setUser(null);
    setDropdownOpen(false);
    navigate("/home");
    toast.info("ƒê√£ ƒëƒÉng xu·∫•t th√†nh c√¥ng.");
  };

  useEffect(() => {
    const loggedOut = sessionStorage.getItem("loggedOut");
    if (loggedOut === "true") {
      toast.info("ƒê√£ ƒëƒÉng xu·∫•t th√†nh c√¥ng.");
      sessionStorage.removeItem("loggedOut");
    }
  }, []);

  const handleChange = (e) => {
    setForm({ ...form, [e.target.name]: e.target.value });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    if (form.password.length < 6) {
      toast.error("M·∫≠t kh·∫©u ph·∫£i c√≥ √≠t nh·∫•t 6 k√Ω t·ª±");
      return;
    }
    try {
      let res;
      if (mode === "register") {
        res = await register(form);
        toast.success("ƒêƒÉng k√Ω th√†nh c√¥ng. Vui l√≤ng ƒëƒÉng nh·∫≠p.");
        setMode("login");
        return;
      } else if (mode === "login") {
        res = await login(form);
        const token = res.data.token;
        if (!token) {
          toast.error("ƒêƒÉng nh·∫≠p th·∫•t b·∫°i: Kh√¥ng nh·∫≠n ƒë∆∞·ª£c token.");
          return;
        }
        const decoded = jwtDecode(token);
        localStorage.setItem("token", token);
        localStorage.setItem("user", JSON.stringify(decoded));
        setUser(decoded);
        setShowLogin(false);
        toast.success("ƒêƒÉng nh·∫≠p th√†nh c√¥ng!");
        navigate("/home");
      }
      setForm({ name: "", email: "", password: "", phone: "", address: "" });
    } catch (err) {
      toast.error("L·ªói: " + (err.response?.data?.message || "Email ho·∫∑c t√†i kho·∫£n kh√¥ng ƒë√∫ng"));
    }
  };

  useEffect(() => {
    if (showLogin) {
      document.body.style.overflow = "hidden";
      document.addEventListener("mousedown", handleClickOutside);
    } else {
      document.body.style.overflow = "auto";
      document.removeEventListener("mousedown", handleClickOutside);
    }
    return () => {
      document.body.style.overflow = "auto";
      document.removeEventListener("mousedown", handleClickOutside);
    };
  }, [showLogin]);

  useEffect(() => {
    const handleOutsideClick = (event) => {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target)) {
        setDropdownOpen(false);
      }
    };
    if (isDropdownOpen) {
      document.addEventListener("mousedown", handleOutsideClick);
    }
    return () => {
      document.removeEventListener("mousedown", handleOutsideClick);
    };
  }, [isDropdownOpen]);

  return (
    <>
      <ToastContainer
        position="top-center"
        autoClose={3000}
        closeOnClick
        pauseOnHover={false}
        draggable={false}
        transition={Slide}
        style={{ zIndex: 99999 }}
      />
      <header className="w-full bg-gray-300 z-50 relative">
        <div className="w-full flex items-center justify-between px-4 py-7 relative">
          <div className="flex-shrink-0 absolute left-4 top-1/2 -translate-y-1/2">
            <Link to="/home" className="logo">
              <img
                src={logo}
                alt="Cholimex"
                className="w-[105px] h-auto rounded-md"
              />
            </Link>
          </div>

          <nav className="flex-1 flex justify-center gap-15">
            <Link to="/home" className="text-sm font-bold uppercase text-[#dd3333]">Trang ch·ªß</Link>
            <Link to="/about" className="text-sm font-bold uppercase text-black hover:text-[#dd3333]">Gi·ªõi thi·ªáu</Link>
            <Link to="/products" className="text-sm font-bold uppercase text-black hover:text-[#dd3333]">S·∫£n ph·∫©m</Link>
            <Link to="/contact" className="text-sm font-bold uppercase text-black hover:text-[#dd3333]">Th∆∞ vi·ªán ·∫©m th·ª±c</Link>
            <Link to="/contact" className="text-sm font-bold uppercase text-black hover:text-[#dd3333]">40 nƒÉm</Link>
          </nav>

          <div className="absolute right-4 top-1/2 -translate-y-1/2" ref={dropdownRef}>
            {user ? (
              <div className="cursor-pointer select-none" onClick={() => setDropdownOpen(!isDropdownOpen)}>
                <div className="flex items-center gap-1 text-sm font-bold uppercase text-black hover:text-[#dd3333]">
                  <span role="img" aria-label="user">üë§</span>
                  <span>Xin ch√†o, {user?.name || user?.email || "User"}</span>
                </div>
                {isDropdownOpen && (
                  <div className="absolute right-0 mt-2 w-48 bg-white shadow-lg rounded-md z-10">
                    <Link to="/profile">
                      <button className="block w-full text-left px-4 py-2 hover:bg-gray-100">Th√¥ng tin t√†i kho·∫£n</button>
                    </Link>
                    <button onClick={handleLogout} className="block w-full text-left px-4 py-2 hover:bg-gray-100">ƒêƒÉng xu·∫•t</button>
                  </div>
                )}
              </div>
            ) : (
              <button onClick={toggleLogin} className="text-sm font-bold uppercase text-black hover:text-[#dd3333]">
                ƒêƒÉng Nh·∫≠p
              </button>
            )}
          </div>
        </div>
      </header>

      {showLogin && (
        <div className="fixed inset-0 bg-black/50 backdrop-blur-sm z-[9999] flex items-center justify-center">
          <div ref={modalRef} className="bg-white rounded-lg shadow-xl p-6 w-full max-w-md relative">
            <button onClick={() => setShowLogin(false)} className="absolute top-2 right-2 text-gray-500 hover:text-red-600 text-xl">&times;</button>
            <h2 className="text-xl font-bold mb-4 text-center">
              {mode === "login" ? "ƒêƒÉng nh·∫≠p" : "ƒêƒÉng k√Ω t√†i kho·∫£n"}
            </h2>
            <form onSubmit={handleSubmit} className="space-y-4">
              {mode === "register" && (
                <>
                  <input name="name" placeholder="H·ªç t√™n" value={form.name} onChange={handleChange} required className="w-full border rounded px-3 py-2" />
                  <input name="phone" placeholder="S·ªë ƒëi·ªán tho·∫°i" value={form.phone} onChange={handleChange} required className="w-full border rounded px-3 py-2" />
                  <input name="address" placeholder="ƒê·ªãa ch·ªâ" value={form.address} onChange={handleChange} required className="w-full border rounded px-3 py-2" />
                </>
              )}
              <input type="email" name="email" placeholder="Email" value={form.email} onChange={handleChange} required className="w-full border rounded px-3 py-2" />
              <input type="password" name="password" placeholder="M·∫≠t kh·∫©u" value={form.password} onChange={handleChange} required className="w-full border rounded px-3 py-2" />
              <button type="submit" className="w-full bg-[#dd3333] text-white font-bold py-2 rounded hover:bg-red-600 transition">
                {mode === "login" ? "ƒêƒÉng nh·∫≠p" : "ƒêƒÉng k√Ω"}
              </button>
            </form>
            <div className="text-center mt-4 text-sm">
              {mode === "login" ? (
                <p>Ch∆∞a c√≥ t√†i kho·∫£n? <button onClick={() => setMode("register")} className="text-[#dd3333] font-semibold hover:underline">ƒêƒÉng k√Ω</button></p>
              ) : (
                <p>ƒê√£ c√≥ t√†i kho·∫£n? <button onClick={() => setMode("login")} className="text-[#dd3333] font-semibold hover:underline">ƒêƒÉng nh·∫≠p</button></p>
              )}
            </div>
          </div>
        </div>
      )}
    </>
  );
}

export default Header;
